const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let arrayExpr = expr.match(/.{10}/g);
    let arrayMorse = arrayExpr.map(function(item){
        return item.replace(/^0*/g,'')
    })
    let result = '';
    arrayMorse.forEach(element => {
        if(element == '**********') result+=' '
        else {
            let code  = element.replace(/11/g,'-').replace(/10/g,'.')
            result+=MORSE_TABLE[code]
        }
    });
    return result
}


module.exports = {
    decode
}